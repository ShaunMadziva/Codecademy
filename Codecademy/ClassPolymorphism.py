# Polymorphism is the term used to describe the same syntax 
# doing different actions depending on the type of data.
#Below len() is called on different data types.

a_list = [1, 18, 32, 12]
a_dict = {'value': True}
a_string = "Polymorphism is cool!"

print(len(a_list))
print(len(a_dict))
print(len(a_string))

